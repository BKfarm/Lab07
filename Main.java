public class Main
{
    public static void main(String[] args)
    {
        for (int x = 1; x < 31; x++)
        {
            System.out.println(x);
        }
        for (int x = 30; x > 0; x--)
        {
            System.out.println(x);
        }
        for (int x = 0; x < 21; x += 3)
        {
            System.out.println(x);
        }
        for (int x = 10; x > 0; x-= 2)
        {
            System.out.println(x);
        }
    }

}